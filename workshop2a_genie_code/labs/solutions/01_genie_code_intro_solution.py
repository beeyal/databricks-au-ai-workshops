from pyspark.sql.window import Window

# Daily totals first
df_daily_totals = (
    df_meters
    .groupBy("nmi", "interval_date")
    .agg(F.sum("read_kwh").alias("daily_kwh"))
)

# Rolling 7-day window ordered by date
window_7d = (
    Window.partitionBy("nmi")
    .orderBy(F.col("interval_date").cast("long"))
    .rowsBetween(-6, 0)          # current row + 6 previous rows = 7 days
)

df_rolling = (
    df_daily_totals
    .withColumn("rolling_7d_avg_kwh", F.round(F.avg("daily_kwh").over(window_7d), 3))
    .orderBy("nmi", "interval_date")
)

