def calculate_load_factor(peak_kwh: float, total_kwh: float, periods: int = 48) -> float:
    """Calculate the load factor for a NEM meter over a given period.

    The load factor measures how consistently a meter's energy consumption is spread
    across all intervals. A load factor of 1.0 means perfectly flat consumption;
    values closer to 0 indicate high peak-to-average ratio (peaky demand).

    In the NEM, load factor is used by network operators to assess demand diversity
    and by retailers to profile customer consumption behaviour.

    Args:
        peak_kwh: The maximum single-interval energy read in kWh.
        total_kwh: The total energy consumed across all intervals in kWh.
        periods: Number of intervals in the analysis period (default 48 = one day).

    Returns:
        Load factor as a float between 0.0 and 1.0, rounded to 4 decimal places.
        Returns 0.0 if peak_kwh is zero or negative.
    """
    if peak_kwh <= 0:
        return 0.0
    average_kwh = total_kwh / periods
    return round(average_kwh / peak_kwh, 4)


