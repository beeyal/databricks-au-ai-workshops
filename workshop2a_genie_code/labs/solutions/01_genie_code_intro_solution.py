from pyspark.sql.window import Window

def flag_missing_intervals(df, nmi_col: str = "nmi", date_col: str = "interval_date", interval_col: str = "interval_number"):
    """Flag rows where the interval_number jumps by more than 1 within an NMI+date group.

    A gap greater than 1 between consecutive interval numbers indicates a missing
    30-minute interval in the NEM12 file — a data quality issue that must be resolved
    before submitting to AEMO.

    Args:
        df: PySpark DataFrame containing interval reads.
        nmi_col: Column name for the NMI identifier.
        date_col: Column name for the date.
        interval_col: Column name for the interval number.

    Returns:
        DataFrame filtered to only rows where a gap exists before this interval.
        Includes a 'gap_size' column showing the size of the jump.
    """
    window_spec = (
        Window.partitionBy(nmi_col, date_col)
        .orderBy(interval_col)
    )

    return (
        df
        .withColumn("prev_interval", F.lag(interval_col, 1).over(window_spec))
        .withColumn("gap_size", F.col(interval_col) - F.col("prev_interval"))
        .filter(F.col("gap_size") > 1)
        .select(nmi_col, date_col, "prev_interval", interval_col, "gap_size")
    )


gaps = flag_missing_intervals(df_meters)
gap_count = gaps.count()
print(f"Missing interval gaps found: {gap_count}")
if gap_count > 0:
    gaps.show()
else:
    print("No missing intervals — all 48 intervals per NMI per day are present.")
